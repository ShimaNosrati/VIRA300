class ICompanyNameScanner
{
public:

    // =================================================================================================
    // Constructor
    // -------------------------------------------------------------------------------------------------
    ICompanyNameScanner() = default;

    // =================================================================================================
    // Destructor
    // -------------------------------------------------------------------------------------------------
    virtual ~ICompanyNameScanner() = default;

    // =================================================================================================
    // Return the last object name to use
    // -------------------------------------------------------------------------------------------------
    virtual std::wstring ObjectNameGet() const = 0;

    // =================================================================================================
    // Replace the data of the polygonal model
    //
    // Parameter: uniqueId : Unique ID of the polygonal model
    // -------------------------------------------------------------------------------------------------
    virtual void PolygonalModelReplace( TUniqueID uniqueId_ ) = 0;

    // =================================================================================================
    // Request for a end of scan to be made in the main thread
    // -------------------------------------------------------------------------------------------------
    virtual void ScanningEndInMainThreadRequest() = 0;

    // =================================================================================================
    // Return to close the Scanning dialog
    // -------------------------------------------------------------------------------------------------
    virtual void ScanningDlgCloseRequest() = 0;

    // =================================================================================================
    // Notification to the plug-in of the Scanning dialog been closed
    // -------------------------------------------------------------------------------------------------
    virtual void ScanningDlgClosed() = 0;
};
